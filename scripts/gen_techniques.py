def generate_file(filename, title, tags, concept, template, when_to_use):
    tag_yaml = "\n".join(f"- {t}" for t in ["techniques"] + tags)
    content = f"""---
tags:
{tag_yaml}
---
# {title}

## 개념

{concept}

## 프롬프트 템플릿

```
{template}
```

## 적합한 상황

{when_to_use}
"""
    path = TECHNIQUES_DIR / f"{filename}.md"
    path.write_text(content, encoding="utf-8")
    return path


