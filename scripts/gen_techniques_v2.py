def generate_file(t):
    tag_yaml = "\n".join(f"- {tag}" for tag in ["techniques"] + t["tags"])
    content = f"""---
tags:
{tag_yaml}
---
# {t["title"]}

## 개념

{t["concept"]}

## 핵심 원리

{t["principles"]}

## 프롬프트 템플릿

```
{t["template"]}
```

## 실전 예시

{t["example"]}

## 변형 및 조합

{t["combinations"]}

## 주의사항

{t["caveats"]}

## 적합한 상황

{t["when_to_use"]}

## 참고 자료

{t["references"]}
"""
    path = TECHNIQUES_DIR / f"{t['filename']}.md"
    path.write_text(content, encoding="utf-8")
    return path


